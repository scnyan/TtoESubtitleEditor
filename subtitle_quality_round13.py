BAD_ENDINGS = ("が。", "を。", "に。", "と。", "の。", "ので。", "ため。", "こと。", "もの。", "でも。")


def clean_text(text):
    output = text or ""
    output = re.sub(r"\({2,}（笑）\){2,}", "（笑）", output)
    output = re.sub(r"（\s*（笑）\s*）", "（笑）", output)
    output = output.replace("[laughter]", "（笑）").replace("[laughter and screaming]", "（笑）")
    output = re.sub(r"\s+", " ", output).strip()
    for old, new in TERM_FIXES:
        output = output.replace(old, new)
    return output


def word_count(text):
    return len(re.findall(r"[A-Za-z0-9']+", text or ""))


def ja_len(text):
    return len(re.sub(r"\s+", "", text or ""))


def issue_flags(row):
    flags = []
    original = row.get("original", "")
    ja = row.get("translation_raw", "").strip()
    if ja in {"えっと。", "こんにちは。", "はは。"}:
        return []
    if not ja:
        flags.append("empty_translation")
    if any(ja.endswith(ending) for ending in BAD_ENDINGS):
        flags.append("likely_fragment")
    en_words = word_count(original)
    if en_words >= 18 and ja_len(ja) < en_words * 1.25:
        flags.append("likely_undertranslated")
    connectors = len(re.findall(r"\b(?:and|but|so|because|when|if|then|also|or|which|that)\b|,", original, flags=re.I))
    if connectors >= 3 and ja_len(ja) < en_words * 1.55:
        flags.append("connector_gap_risk")
    for first, second in zip(row.get("shorts_segments", []), row.get("shorts_segments", [])[1:]):
        if first.get("text") == second.get("text") or first.get("en") == second.get("en"):
            flags.append("shorts_duplicate")
        if first.get("end_ms", 0) > second.get("start_ms", 0):
            flags.append("shorts_overlap")
        if first.get("end_ms", 0) - first.get("start_ms", 0) < 650:
            flags.append("too_fast")
    return sorted(set(flags))


def main():
    data = json.loads(DATA_PATH.read_text(encoding="utf-8"))
    report = []

    for row in data["segments"]:
        row_id = str(row.get("id"))
        before = row.get("translation_raw", "")
        fixed = MANUAL_TRANSLATION_FIXES.get(row_id, before)
        fixed = clean_text(fixed)
        row["translation_raw"] = fixed
        row["subtitle_ja"] = fixed
        row["translation"] = fixed if row.get("speaker") == "SFX" else f"({row.get('speaker', 'Group')}) {fixed}"
        if fixed != before:
            row["translation_status"] = "round13_manual_quality_fix"
            report.append({"id": row_id, "type": "manual_fix", "old": before, "new": fixed})

    for row in data["segments"]:
        refresh_row(row)

    for row in data["segments"]:
        flags = issue_flags(row)
        row["quality_flags"] = flags
        if flags:
            row["translation_status"] = "needs_quality_review"
            report.append(
                {
                    "id": row.get("id"),
                    "start": row.get("start"),
                    "end": row.get("end"),
                    "type": ",".join(flags),
                    "original": row.get("original", ""),
                    "ja": row.get("translation_raw", ""),
                    "shorts": " / ".join(seg.get("text", "") for seg in row.get("shorts_segments", [])),
                }
            )

    DATA_PATH.write_text(json.dumps(data, ensure_ascii=False, indent=2), encoding="utf-8")
    write_csv(data)

    fields = ["id", "start", "end", "type", "original", "ja", "shorts", "old", "new"]
    with REPORT_PATH.open("w", encoding="utf-8-sig", newline="") as file:
        writer = csv.DictWriter(file, fieldnames=fields)
        writer.writeheader()
        writer.writerows({field: item.get(field, "") for field in fields} for item in report)
    print("round13 report", len(report), REPORT_PATH)


if __name__ == "__main__":
    main()
